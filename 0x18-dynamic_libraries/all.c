#include <stdio.h>
/**
 * add - add two integer
 * @a: first int
 * @b: secont int
 */

int add(int a, int b)
{
	return(a + b);
}
/**
 * sub -subtract two integer
 * @a: first int
 * @b: secont int
 */

int sub(int a, int b)
{
	return(a - b);
}

/**
 * mul - multiply two integer
 * @a: first int
 * @b: secont int
 */

int mul(int a, int b)
{
	return(a * b);
}

/**
 * div - divide two integer
 * @a: first int
 * @b: secont int
 */

int div(int a, int b)
{
	return(a / b);
}

/**
 * mod - mod of two integer
 * @a: first int
 * @b: secont int
 */

int mod(int a, int b)
{
	return(a % b);
}
